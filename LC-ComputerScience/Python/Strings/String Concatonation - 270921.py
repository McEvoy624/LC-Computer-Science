# String Concatonation
word1 = '4'
word2 = '3'

subject = word1 + word2
print(subject)
# the result will be 43 as python sees 43 as strings not numbers

word3 = 4
word4 = 3

answer = word3 + word4
print(answer)
# python now sees 4 and 3 as numbers instead of words because there is no ''
